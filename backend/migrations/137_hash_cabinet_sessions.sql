-- 137: хеширование токенов клиентских сессий (аудит безопасности 22.06).
--
-- Контекст: таблица sessions (кабинет клиента) хранила Bearer-токен в открытом виде.
-- Утечка дампа БД давала злоумышленнику рабочие токены всех залогиненных клиентов
-- (до 30 дней TTL). Admin-сессии (user_sessions.refresh_token_hash) уже хешированы,
-- клиентские — нет. Код переведён на sha256 (routes/cabinet-auth.js, dual-lookup).
--
-- Эта миграция переводит УЖЕ существующие plaintext-токены в sha256, чтобы в БД не
-- осталось открытых значений. Код параллельно принимает оба формата (sha256 и legacy
-- plaintext), поэтому порядок «деплой кода ↔ запуск миграции» не рвёт живые сессии.
--
-- Идемпотентность: миграция выполняется ровно один раз (трекинг в _migrations).
-- raw-токен и его sha256 оба 64-символьные hex → отличить их нельзя, поэтому повторный
-- прогон НЕДОПУСТИМ (двойной хеш сломал бы все сессии). Защита — таблица _migrations.

CREATE EXTENSION IF NOT EXISTS pgcrypto;

UPDATE sessions
   SET token = encode(digest(token, 'sha256'), 'hex')
 WHERE token IS NOT NULL;
