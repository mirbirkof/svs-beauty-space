-- 119: урізаємо права ролі "Майстер" до мінімуму.
-- ПРОБЛЕМА (безпека): майстер мав зайві права — tasks/kb/incidents/surveys/qc/
-- documents/portfolio/saas/esign/medical.read+write. Через них з кабінету майстра
-- можна було дістатись хазяйських системних розділів CRM (хай і не напряму).
-- Власник хоче: майстер бачить ТІЛЬКИ свій графік / своїх клієнтів / свою зарплату /
-- свої послуги. Все інше — заборонено на рівні сервера (кожен модуль гейтиться правом).
--
-- Мінімальний набір:
--   schedule.read    → Мій графік (журнал тільки своїх записів)
--   cashbox.read.own → Моя зарплата (власні нарахування)
--   reports.own      → власні звіти/оверв'ю (фільтр по своєму master_id)
--   bookings.own     → власні записи
--   booking.read     → каталог послуг для запису
-- Кабінет майстра (/api/me/*) працює тільки на авторизації + фільтрі по своєму master_id,
-- тож додаткові права не потрібні. Прибираємо ВСЕ зайве.

UPDATE roles
   SET permissions = '["schedule.read","cashbox.read.own","reports.own","bookings.own","booking.read"]'::jsonb
 WHERE code = 'master';
