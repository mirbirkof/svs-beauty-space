-- 139: онлайн-деньги в кассе без привязки к смене (аудит 22.06, Critical).
--
-- Ручные продажи пробиваются в открытую смену (cash_shifts). Но Mono-оплаты визита/
-- предоплаты, продажа сертификатов и абонементов приходят 24/7 без открытой смены —
-- раньше они вообще не попадали в cash_operations, из-за чего касса/P&L/ДДС не сходились
-- с реально полученными деньгами.
--
-- Снимаем NOT NULL с shift_id: онлайн-операции пишутся с shift_id = NULL (деньги на
-- счёте/карте, а не в денежном ящике). В Z-отчёт смены они не входят, но в выручку и
-- ДДС — да (дашборд агрегирует по категории/дате, а не по смене).
--
-- Идемпотентность online-операций обеспечивает уже существующий уникальный индекс
-- ux_cash_operations_ext_ref (миграция 031). Данные не меняются — только ослабляем
-- ограничение, существующие 1799 строк все имеют shift_id и не нарушаются.

ALTER TABLE cash_operations ALTER COLUMN shift_id DROP NOT NULL;
